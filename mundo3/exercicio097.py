def escrever_mensagem_especial (frase):
    tamanho_linhas = len(frase) + 2
    print("~" * tamanho_linhas)
    print(f" {frase}")
    print("~" * tamanho_linhas)

escrever_mensagem_especial("Eu gosto de Ana Ellen")
escrever_mensagem_especial("Eu quero mudar")
escrever_mensagem_especial("28/08/2025")
