from math import sqrt
numero_usuario = int(input("Digite um número: "))
dobro = 2 * numero_usuario
triplo = 3 * numero_usuario
raiz_quadrada = sqrt(numero_usuario)

print(f"O dobro de \'{numero_usuario}\' é {dobro}")
print(f"O triplo de \'{numero_usuario}\' é {triplo}")
print(f"A raiz de \'{numero_usuario}\' é {raiz_quadrada}")
